# creating a list of strings.

Animal = ["panda", "zebra", "monkey", "panda"]

print("This is value stored in Animal String: ", Animal)
print("This is Lenght : ", len(Animal))
print("\n")

print("Access 1st index in Animal String", Animal[1])
print("Access Last index in Animal String", Animal[-1])
print("\n")

print("Access Frist index's Frist word in Animal String", Animal[0][0])
print("\n")

print("Access Frist index's last word in Animal String", Animal[0][-1])
print("\n")

print("Insert this(dog) in Animal String's 2nd index :")
Animal.insert(2, "dog")
print("This is updated value stored in Animal String: ", Animal)
print("\n")

